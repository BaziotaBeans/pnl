    //Armazena todas as palavras num ArrayList
    private ArrayList getPalavras()
    {
        ArrayList palavras = new ArrayList();
        List<String>lista = Consultar.getTodasPalavras();
        for(int i=0; i < lista.size(); i++) palavras.add(lista.get(i));
        return palavras;
    }
    
    
    private void bindData()
    {
        getPalavras().stream().forEach((palavra)->{
            defaultListModel.addElement(palavra);
        });
        superJList.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    //Filta ou pesauisa os dados
    private void searchFilter(String searchTerm)
    {
        DefaultListModel filteredItems = new DefaultListModel();
        ///ArrayList palavras = getPalavras();
        
        getPalavras().stream().forEach((palavra) ->{
            String palavraInicial = palavra.toString().toLowerCase();
            String primeira_ocorrencia1 = String.valueOf(palavraInicial.charAt(0));
            String primeira_ocorrencia2 = String.valueOf(searchTerm.toLowerCase().charAt(0));
            if(primeira_ocorrencia1.equals(primeira_ocorrencia2)) filteredItems.addElement(palavra);
        });
        defaultListModel=filteredItems;
        superJList.setModel(defaultListModel);
        if (texto_perguntaJTX1.getText().isEmpty())
        {
            defaultListModel.clear();
            superJList.setModel(defaultListModel);
        }
    } 

    public void setarFlag(boolean[] vector, int index) {
        for (int i = 0; i < 8 - 1; i++) {
            if (i == index) {
                continue;
            } else {
                vector[i] = false;
            }
        }
    }

    public void inicializarFlag(boolean[] vector) {
        for (int i = 0; i < 8; i++) {
            vector[i] = false;
        }
    }

