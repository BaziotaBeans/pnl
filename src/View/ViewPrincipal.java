    private void Analisar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Analisar_btnActionPerformed
        String texto = texto_perguntaJTX.getText().toLowerCase().toString();
        
        if (validar(texto)) 
        {
            JOptionPane.showMessageDialog(null, "pergunta inválida!\nPor favor digite uma pergunta válida", "", JOptionPane.ERROR_MESSAGE);
            texto_perguntaJTX.setText("");
        } 
        else if (texto.isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, "O senhor(a) não prencheu nada Por favor digite uma pergunta!", "", JOptionPane.ERROR_MESSAGE);
            texto_perguntaJTX.setText("");
        }
        else if(String.valueOf(texto.charAt(texto.length() - 1)).equalsIgnoreCase("?"))
        {
            String resultado = Consultar.consultarPergunta(texto);
            if(resultado.isEmpty())
            {
                texto_perguntaJTX.setVisible(false);
                Analisar_btn.setVisible(false);
                responder_btn.setVisible(true);
                paraPerguntaJLabel.setVisible(true);
                respostaJTX.setVisible(true);
                pergunta.setVisible(true);
                pergunta.setText("Pergunta: "+texto_perguntaJTX.getText().toString());
                paraPergunta.setVisible(false);
                paraPergunta2.setVisible(false);
            }
            else
            {
                resultadoPerguntaTArea.setText(" Resposta: "+resultado);
                
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Por favor verifique se colocou o ponto de interrogação no devido lugar.", "", JOptionPane.ERROR_MESSAGE);
            respostaJTX.setText("");
        }
    }//GEN-LAST:event_Analisar_btnActionPerformed

