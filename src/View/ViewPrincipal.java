    private void analisar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_analisar_btnActionPerformed
        // TODO add your handling code here:        
        String txt = textoJTX.getText().toLowerCase().toString();
        char primeiroMembro;
        System.out.println(txt);
        if (validar(txt)) 
        {
            JOptionPane.showMessageDialog(null, "Frase ou pergunta inválida!\nPor favor digite uma frase ou pergunta válida", "", JOptionPane.ERROR_MESSAGE);
            textoJTX.setText("");
        } 
        else if (txt.isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, "O senhor(a) não prencheu nada Por favor digite uma frase ou uma pergunta!", "", JOptionPane.ERROR_MESSAGE);
            textoJTX.setText("");
        }
        else if (String.valueOf(txt.charAt(txt.length() - 1)).equalsIgnoreCase(".") || String.valueOf(txt.charAt(txt.length() - 1)).equalsIgnoreCase("!") || String.valueOf(txt.charAt(txt.length() - 1)).equalsIgnoreCase(""))
        {
            String consulta = txt.trim().replace(" ", ",").replace(".", "").replace("!", "");
            //String resultado = QueryGramatical.consultar_arvore_sintatica(consulta);
            String[] vectorPalavras = consulta.split(",");
            List<String> lista = Consultar.palavrasNovas(vectorPalavras);
            if(!lista.isEmpty())
            {
                int ok = JOptionPane.showConfirmDialog(null, "Não tenho essa frase na minha base de conhecimento\nPor favor podes ajudar-me a cadastrar na minha Base de conhecimento?");
                if (ok == 0)
                {
                    new Alimentacao(lista, txt).setVisible(true);
                    dispose();
                }
            }
            else
            {
                resultadoTArea.setText(Consultar.consultaGeral(consulta));
                tipoJLabel.setVisible(true);
                
                if(String.valueOf(txt.charAt(txt.length() - 1)).equalsIgnoreCase("!"))
                    tipoJLabel.setText("Exclamação");
                else if(String.valueOf(txt.charAt(txt.length() - 1)).equalsIgnoreCase(".")) tipoJLabel.setText("Afirmação");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Por favor verifique se colocou o ponto no devido lugar.", "", JOptionPane.ERROR_MESSAGE);
            respostaJTX.setText("");
        }
    }//GEN-LAST:event_analisar_btnActionPerformed

